/*
    【变量】 可以改变的向量存储

        声明：数据类型 + 变量名称
 
        赋值：变量名称 = 数据

        使用：直接访问变量名称即可 sout

        初始化：使用变量之前，必须给变量赋值
 */

public class variable {
    public static void main(String[] args) {

        // 方法一
        String name;

        name = "Java";

        System.out.println(name);
        
        // 方法二
        String username = "java";

        System.out.println(username);
    }
}